// Using String.fromCharCode to create characters
let charA = String.fromCharCode(65); // "A"
console.log(charA); // Output: A

// Unicode escape sequence for "A"
let unicodeA = '\u0041';
console.log(unicodeA); // Output: A

// Combining characters using String.fromCharCode
let hello = String.fromCharCode(72, 101, 108, 108, 111); // "Hello"
console.log(hello); // Output: Hello

// Unicode escape sequence for "Hello"
let unicodeHello = '\u0048\u0065\u006C\u006C\u006F';
console.log(unicodeHello); // Output: Hello
